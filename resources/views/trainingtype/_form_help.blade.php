<strong>Name</strong>
<p>A name of the training type category is required.</p>

<strong>Status</strong>
<p>The state of the activity status is required.</p>
<ul class="browser-default">
    <li>Active - Allows type to be selected when modifying a training.</li>
    <li>Inactive - Type is unavailable when modifying a training</li>
</ul>

<strong>Description</strong>
<p>The description of the training type category is optional.</p>

<strong>Create/Update button</strong>
<p>Save the training type.</p>
